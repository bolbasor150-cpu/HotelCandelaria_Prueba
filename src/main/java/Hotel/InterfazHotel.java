    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
          try {

        if (txtCliente.getText().trim().isEmpty() ||
            txtDni.getText().trim().isEmpty() ||
            txtCorreo.getText().trim().isEmpty() ||
            cboHabitacion.getSelectedItem() == null ||
            txtMonto.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Complete todos los campos");
            return;
        }

        if (txtDni.getText().length() != 8) {
            JOptionPane.showMessageDialog(this, "DNI debe tener 8 dígitos");
            return;
        }


        Cliente cliente = new Cliente(
                txtCliente.getText(),
                txtDni.getText(),
                txtCorreo.getText()
        );

        Empleado empleado = new Empleado("", "", "");

        int numero = Integer.parseInt(cboHabitacion.getSelectedItem().toString());

         Habitacion habitacion = Contenedor.getInstancia().buscarHabitacion(numero);

          if (!habitacion.isDisponible()) {
           JOptionPane.showMessageDialog(this, "Habitación ocupada");
          return;
          }

          habitacion.setDisponible(false);

        Pago pago = new Pago(
                Double.parseDouble(txtMonto.getText()),
                cmbMetodoPago.getSelectedItem().toString()
        );

        Reserva reserva = ReservaFactory.crearReserva(
        cliente,
        empleado,
        habitacion,
        pago
        );

        Contenedor.getInstancia().insertar(reserva);

        txtAreaResultado.setText(Contenedor.getInstancia().mostrar());
        
        contadorId++;
        mostrarId();

        limpiar();
        cargarHabitaciones();
        JOptionPane.showMessageDialog(this, "Reserva registrada");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al registrar");
    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

