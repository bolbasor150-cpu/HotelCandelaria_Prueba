private void cargarHabitaciones() {

    cboHabitacion.removeAllItems();

    String tipo = cboTipoHabitacion.getSelectedItem().toString();

    for (Habitacion h : Contenedor.getInstancia().getHabitaciones()) {

        if (h.getTipo().equals(tipo) && h.isDisponible()) {

            cboHabitacion.addItem(String.valueOf(h.getNumero()));

        }
    
    }

}
   
