    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        if (txtEmpleado.getText().trim().isEmpty() ||
        txtDniEmpleado.getText().trim().isEmpty() ||
        cboCargo.getSelectedItem() == null) {

        JOptionPane.showMessageDialog(this, "Complete todos los campos");
        return;
    }

    if (txtDniEmpleado.getText().length() != 8) {
        JOptionPane.showMessageDialog(this, "DNI debe tener 8 dígitos");
        return;
    }

    Empleado empleado = new Empleado(
        txtEmpleado.getText(),
        txtDniEmpleado.getText(),
        cboCargo.getSelectedItem().toString()
    );

    Contenedor.getInstancia().insertarEmpleado(empleado);

    txtAreaEmpleados.setText(Contenedor.getInstancia().mostrarEmpleados());

    limpiar();

    JOptionPane.showMessageDialog(this, "Empleado registrado");

    }//GEN-LAST:event_btnAñadirActionPerformed

