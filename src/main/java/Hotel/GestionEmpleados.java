    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Empleado e = Contenedor.getInstancia().buscarEmpleado(txtDniEmpleado.getText());

    if (e != null) {
        txtAreaEmpleados.setText(e.mostrarInformacion());
    } else {
        JOptionPane.showMessageDialog(this, "No encontrado");
    }
    
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (Contenedor.getInstancia().eliminarEmpleado(txtDniEmpleado.getText())) {
        JOptionPane.showMessageDialog(this, "Eliminado");
    } else {
        JOptionPane.showMessageDialog(this, "No encontrado");
    }

    txtAreaEmpleados.setText(Contenedor.getInstancia().mostrarEmpleados());
    limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed

